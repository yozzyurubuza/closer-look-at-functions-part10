// First-Class vs Higher-Order Functions

/*
First Class Functions
- Treated as first-class citizens
- Are simply values
- Just another "type" of object

- Store functions in variables
- Pass functions as arguments to other functions (addEventListener)
- Return functions from Functions 
- Call methods on functions

Higher-Order Functions
- A function that receives another function as an argument, that returns a new function (addEventListener)
- This is only possible because of first-class function

*/
